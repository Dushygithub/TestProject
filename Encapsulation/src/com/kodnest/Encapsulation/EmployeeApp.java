package com.kodnest.Encapsulation;

public class EmployeeApp {
	private int id;
	private String name;
	private int age;
	
	public void setdata(int id,String name,int age)
	{
		this.id=id;
		this.name=name;
		this.age=age;
		
	}
	public int getId()
	{
		return id;
	}
	public String getname()
	{
		return name;
	}
	public int getage()
	{
		return age;
	}
	
			

}
